function HPplus(){
	//pull in value
	var HPval = document.getElementById("HP").innerHTML
	
	//increment value
	HPval++;
	
	//set new value on page
	document.getElementById("HP").innerHTML = HPval;
}

function HPminus(){
	//pull in value
	var HPval = document.getElementById("HP").innerHTML
	
	//increment value
	HPval--;
	
	//set new value on page
	document.getElementById("HP").innerHTML = HPval;
}

